public class SwitchNotice {
    public static void main(String[] args){
        int num = 2;
		switch (num) {
            case 1:
				System.out.println("nihao");
				break;
			case 2:
				System.out.println("wohao");
				//break
			case 3:
				System.out.println("dajiahao");
				break;
			default:
				System.out.println("tahao,woyehaoao");
				break;
		}
	}
}
